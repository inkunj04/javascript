
let a = 10;
let b = 20;
console.log("a+b= ",a+b);
console.log("a-b= ",a-b);
console.log("a*b= ",a*b);
console.log("a/b= ",a/b);
console.log("a%b= ",a%b);   
console.log("a**b= ",a**b); // a to the power of b

console.log("a++= ",a++); // post increment, it will return the value of a and then increment it
console.log("a--= ",a--); // post decrement, it will return the value of a and then decrement it    




